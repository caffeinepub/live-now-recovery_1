export interface GeoJSONPoint {
  type: "Feature";
  geometry: {
    type: "Point";
    coordinates: [number, number];
  };
  properties: Record<string, unknown>;
}

export interface GeoJSONFeatureCollection {
  type: "FeatureCollection";
  features: GeoJSONPoint[];
}

export function providersToGeoJSON(
  providers: ProviderWithStatus[],
): GeoJSONFeatureCollection {
  const features: GeoJSONPoint[] = providers.map((p) => {
    const nowNs = BigInt(Date.now()) * 1_000_000n;
    const isStale = nowNs - p.lastVerified > FOUR_HOURS_NS;
    return {
      type: "Feature",
      geometry: {
        type: "Point",
        coordinates: [p.lng, p.lat],
      },
      properties: {
        id: p.id,
        name: p.name,
        lat: p.lat,
        lng: p.lng,
        status: p.status,
        isLive: p.isLive && !isStale,
        isStale,
      },
    };
  });

  return { type: "FeatureCollection", features };
}

export function handoffCountsToHeatmapGeoJSON(
  counts: [string, bigint][],
): GeoJSONFeatureCollection {
  const features: GeoJSONPoint[] = [];

  for (const [zip, count] of counts) {
    const coords = ZIP_COORDS[zip];
    if (!coords) continue;
    features.push({
      type: "Feature",
      geometry: {
        type: "Point",
        coordinates: [coords[1], coords[0]], // [lng, lat]
      },
      properties: {
        zip,
        weight: Number(count),
      },
    });
  }

  return { type: "FeatureCollection", features };
}
